"""
== igual a
> maior que
>= maior ou igua que
< menor que
<= menor ou igual que
!= diferente de
"""
num_1 = 2
num_2 = 2

print('==', num_1 == num_2)
print('>', num_1 > num_2)
print('>=', num_1 >= num_2)
print('<', num_1 < num_2)
print('<=', num_1 <= num_2)
print('!=', num_1 != num_2)
